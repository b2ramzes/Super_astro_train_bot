    if not user_data:
        return

    mode = user_data["mode"]
    subcategory = user_data["subcategory"]
    question_data = user_data["questions"][user_data["index"]]
    username=user_data['username']

    # –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Ç–∞–π–º–µ—Ä (–µ—Å–ª–∏ –æ–Ω –µ—Å—Ç—å) –ø–µ—Ä–µ–¥ –ø—Ä–æ–≤–µ—Ä–∫–æ–π –æ—Ç–≤–µ—Ç–∞

        user_progress.pop(chat_id, None)  # –£–¥–∞–ª—è–µ–º –ø—Ä–æ–≥—Ä–µ—Å—Å –∏–≥—Ä–æ–∫–∞
        bot.delete_message(chat_id,pool_id)


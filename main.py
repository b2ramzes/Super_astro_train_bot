def get_user_position(user_id, stats):
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –ø–æ–∑–∏—Ü–∏—é –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤ –æ–±—â–µ–º —Ä–µ–π—Ç–∏–Ω–≥–µ."""
    leaderboard = sorted(
        [(uid, data["–ü—Ä–∞–∫—Ç–∏–∫—É–º"] + data["–°–ø–µ—Ü.–∏–≥—Ä–∞"]) for uid, data in stats.items()],
        key=lambda x: x[1],
        reverse=True
    )
    for position, (uid, _) in enumerate(leaderboard, start=1):
        if uid == str(user_id):
            return position, len(leaderboard)
    return None, len(leaderboard)


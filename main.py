@bot.callback_query_handler(func=lambda call: call.data.startswith('rating_'))
def show_rating(call):
    """–û—Ç–æ–±—Ä–∞–∂–∞–µ—Ç —Ä–µ–π—Ç–∏–Ω–≥ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π."""
    mode = call.data.split('_')[1]
    stats = load_stats()

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –Ω—É–∂–Ω—ã–π —Ä–µ–∂–∏–º –≤ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–µ
    sorted_stats = sorted(
        stats.items(),
        key=lambda x: x[1].get(mode, 0),  # –ï—Å–ª–∏ —Ä–µ–∂–∏–º–∞ –Ω–µ—Ç, –ø–æ–¥—Å—Ç–∞–≤–ª—è–µ–º 0
        reverse=True
    )

    top_users = sorted_stats[:50]
    rating_text = f"üèÜ –¢–æ–ø-50 –ø–æ {mode}:\n"

    for i, (user_id, scores) in enumerate(top_users, start=1):
        score = scores.get(mode, 0)  # –û–ø—è—Ç—å –∂–µ, –µ—Å–ª–∏ –Ω–µ—Ç —Ä–µ–∂–∏–º–∞ ‚Äì 0
        rating_text += f"{i}. ID {user_id} ‚Äî {score} –∏–≥—Ä\n"

    markup = InlineKeyboardMarkup()
    back = InlineKeyboardButton("üîô –ù–∞–∑–∞–¥", callback_data=f'mode_{mode}')
    markup.add(back)

    bot.edit_message_text(
        chat_id=call.message.chat.id,
        message_id=call.message.message_id,
        text=rating_text,
        reply_markup=markup
    )




